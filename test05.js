// Control Statement
// Selection-Condition พิสูจน์ตรวจสอบก่อนที่จะทำงานใดๆ
// if, if-else, if-else-if, switch
 
// if พิสูจน์ครั้งเดียว จริงทำ เท็จไม่ทำ
let x = 20
 
if ( x <= 50 ){
    console.log('Hello...')
}
 
 
// if-else พิสูจน์ครั้งเดียว จริงทำอย่าง เท็จทำอีกอย่าง
 
let data = 'Bangkok'
 
if (data == 'Bangna') {
    //จริงทำของ if
    console.log('Hello Bangkok')
} else {
    //เท็จทำของ else
    console.log('Hello World')
}
 